package com.ccyy.designPattern.behavioral.iterator.spammer;

import com.ccyy.designPattern.behavioral.iterator.iterators.ProfileIterator;
import com.ccyy.designPattern.behavioral.iterator.networks.SocialNetwork;
import com.ccyy.designPattern.behavioral.iterator.profile.Profile;

/**
 * @author: lianghanmao
 * @create: 2022-03-25
 * @description: 消息发送
 **/
public class SocialSpammer {
    public SocialNetwork network;
    public ProfileIterator iterator;

    public SocialSpammer(SocialNetwork network) {
        this.network = network;
    }

    public void sendSpamToFriends(String profileEmail, String message) {
        System.out.println("\nIterating over friends...\n");
        iterator = network.createFriendsIterator(profileEmail);
        while (iterator.hasNext()) {
            Profile profile = iterator.getNext();
            sendMessage(profile.getEmail(), message);
        }
    }

    public void sendSpamToCoworkers(String profileEmail, String message) {
        System.out.println("\nIterating over coworkers...\n");
        iterator = network.createCoworkersIterator(profileEmail);
        while (iterator.hasNext()) {
            Profile profile = iterator.getNext();
            sendMessage(profile.getEmail(), message);
        }
    }

    public void sendMessage(String email, String message) {
        System.out.println("Sent message to: '" + email + "'. Message body: '" + message + "'");
    }
}
